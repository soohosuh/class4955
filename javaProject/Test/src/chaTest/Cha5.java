package chaTest;

public class Cha5 {
	
	public static void main(String[] args) {
		
		// cha4를 스위치문으로 바꾸시오
		int n=2;
		switch(n) {
		case 4 : 
			System.out.println("현재 많은 사람이 있습니다");
			break;
		case 3 :
			System.out.println("현재 인원은 3명 입니다");
			break;
		case 2 :
			System.out.println("현재 인원은 2명 입니다");
			break;
		case 1 :
			System.out.println("현재 인원은 1명 입니다");
		
		
		}
		
		
		
	}

}
